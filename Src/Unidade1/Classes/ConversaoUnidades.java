package Src.Unidade1.Classes;
public class ConversaoUnidades {
  public static double polegadasCentimetros(double polegadas) {
        return 2.54*polegadas;
    }

    public static double pesCentimetros(double pes) {
        return 30.48*pes;
    }

    public static double milhasQuilometros(double milhas) {
        return milhas*1.609;
    }
}
